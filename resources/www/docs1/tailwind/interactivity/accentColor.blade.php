      <p>You can also use variant modifiers to target media queries like responsive breakpoints, dark mode,
        prefers-reduced-motion, and more. For example, use <code>md:accent-pink-500</code> to apply the
        <code>accent-pink-500</code> utility at only medium screen sizes and above.
      </p>
