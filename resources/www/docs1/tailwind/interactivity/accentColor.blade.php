              </svg></span></span>Hover, focus, and other states</a></h3>
      <p>Tailwind lets you conditionally apply utility classes in different states using variant modifiers. For example,
        use <code>hover:accent-pink-500</code> to only apply the <code>accent-pink-500</code> utility on hover.</p>
      <pre
        class="language-html"><code class="language-html"><span class="token tag"><span class="token tag"><span class="token punctuation">&lt;</span>input</span> <span class="token attr-name">type</span><span class="token attr-value"><span class="token punctuation attr-equals">=</span><span class="token punctuation">"</span>checkbox<span class="token punctuation">"</span></span> <span class="token attr-name">class</span><span class="token attr-value"><span class="token punctuation attr-equals">=</span><span class="token punctuation">"</span>accent-pink-300 <span class="code-highlight bg-code-highlight">focus:accent-pink-500</span><span class="token punctuation">"</span></span> <span class="token attr-name">checked</span><span class="token punctuation">&gt;</span></span></code></pre>
      <p>Note, while itâ€™s possible to set an accent color using the <code>hover</code> and <code>active</code>
        modifiers, the resulting color will be slightly different than what you set, as browsers automatically adjust
        the brightness of the accent color for these two states.</p>
      <p>For a complete list of all available state modifiers, check out the <a
          href="/docs/hover-focus-and-other-states">Hover, Focus, &amp; Other States</a> documentation.</p>
